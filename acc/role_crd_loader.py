"""ACC RoleCRDProjector — CRD → file projection (proposal 010 PR-3).

The mirror image of ``operator/internal/filewatch/`` on the Python side.
When ``role_sync.role_source`` is ``crd`` or ``mirror``, this module
polls the Kubernetes API for ``AgentCollective`` resources and writes
their ``spec.roleDefinition`` block to ``roles/<id>/role.yaml`` on the
agent's local filesystem.  The existing
:class:`acc.role_loader.RoleLoader` file-watcher then picks up the
write and triggers role hot-reload as usual — no separate code path
inside the agent's hot loop.

Design choices
--------------

* **Optional ``kubernetes`` dependency.**  We never hard-import the
  ``kubernetes`` library at module level — it's a heavy dep with
  many transitives, and agents running in ``role_source: files`` mode
  (the standalone default) should not need it.  The first call to
  :meth:`RoleCRDProjector.start` lazy-imports ``kubernetes``; if the
  import fails the projector logs a clear error and disables itself.
  Existing file-side behaviour is unaffected.

* **Protocol-based client.**  :class:`CRDClient` is a Protocol so unit
  tests can supply a fake without spinning up envtest or a kind
  cluster.  :class:`KubernetesCRDClient` is the production impl that
  wraps ``kubernetes.client.CustomObjectsApi``.

* **Polling, not watch.**  The Kubernetes ``watch`` API would be more
  efficient but harder to test deterministically.  Polling at
  ``poll_interval_s`` (default 30) matches the file-side watcher's
  cadence and keeps both directions symmetric.

* **Idempotent writes.**  ``project_one()`` reads the existing file
  (if any), compares canonicalised YAML, and only writes when the
  CRD content actually differs.  Prevents reconcile-loop oscillation
  in ``mirror`` mode (PR-4) and avoids spurious mtime bumps.

* **Sentinel comment.**  Generated files carry a header pointing
  back at the source CRD so an operator who ``cat``\\ s the file
  understands why it's there.

Proposal reference
------------------
``010 - Bi-directional file-CRD sync for role definitions.md``,
§5 step 3.
"""

from __future__ import annotations

import asyncio
import logging
import os
from pathlib import Path
from typing import Any, Optional, Protocol

import yaml

logger = logging.getLogger("acc.role_crd_loader")


# Sentinel comment prepended to every CRD-derived role.yaml file.
# The operator-side file watcher's parser ignores comments, so this is
# safe to round-trip through a mirror-mode reconcile.
_SENTINEL_HEADER = """\
# Generated by acc.role_crd_loader from CRD
# AgentCollective: {namespace}/{name}
# Source-of-truth: CRD (role_sync.role_source=crd or mirror).
# Do not edit this file by hand — set role_source=files in
# acc-config.yaml to make local files SOT instead.
# Last projected: {ts}
"""


def _utcnow_iso() -> str:
    """Tiny indirection so tests can monkeypatch the timestamp."""
    from datetime import datetime, timezone
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


